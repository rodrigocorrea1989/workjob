add_shortcode( 'list_total','list_total_shortcode');

function list_total_shortcode(){
	$countTotal=0;
	
	$post_list = get_posts([
    'post_type'      => 'post_type_donation', // basado en el CPT 
    'posts_per_page' => -1
]);
	foreach ( $post_list as $post ) {
	
		$total=$post->total;
		
		$countTotal=$countTotal+$total;
		
	
	}
	
	 	echo "$".$countTotal;
}

